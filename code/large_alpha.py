# f(x) = x^2 -2x + 3
x = 20
alpha = 100
round = 10

for i in range (1, round + 1):
    gradient = 2 * x - 2
    step  = alpha * gradient
    x = x -step

    print(
        f'After {i}, '
        f'x = {x: .3f}, '
        f'f(x, y) = {x**2 - 2 * x + 3: .3f}'
    )

